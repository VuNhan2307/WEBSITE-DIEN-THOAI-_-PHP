1. Sản phẩm chưa qua sử dụng:<br>

    &nbsp&nbsp&nbsp&nbsp Khách hàng được đổi sản phẩm sang model khác nếu sản phẩm chưa qua sử dụng và phải được kiểm tra, xác nhận bởi nhân viên bảo hành của <b>VNBlackBerry</b>.
