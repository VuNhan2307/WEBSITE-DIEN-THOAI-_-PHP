&nbsp&nbsp&nbsp&nbsp Khách hàng được đổi sản phẩm mới cùng loại khi sản phẩm gặp sự cố không thể khắc phục được (do lỗi kỹ thuật của nhà sản xuất). Sản phẩm lỗi chỉ được đổi sau khi đã có xác nhận của kỹ thuật và tuân thủ theo các điều kiện sau đây:<br>

&nbsp&nbsp&nbsp&nbsp    Hàng hóa được xác định nguồn gốc mua tại Hệ thống <b>VNBlackBerry</b> (căn cứ vào hóa đơn mua hàng và tem nhận diện dán trên sản phẩm)<br>
&nbsp&nbsp&nbsp&nbsp    Hàng hóa được xác nhận bị lổi kỹ thuật của Kỹ thuật viên của Trung Tâm Bảo Hành của <b>VNBlackBerry</b>/Hãng<br>
&nbsp&nbsp&nbsp&nbsp    Hàng hóa vẫn còn nguyên tem niêm phong (không bị rách vở hoặc dán lại) của nhà sản xuất hoặc tem của <b>VNBlackBerry</b>.<br>
&nbsp&nbsp&nbsp&nbsp    Hàng hóa nhận lại không bị lổi hình thức (trầy xước, móp méo, ố vàng, vỡ …)<br>
&nbsp&nbsp&nbsp&nbsp    Hàng hóa nhận lại phải còn đầy đủ linh phụ kiện, tặng phẩm kèm theo.<br>
&nbsp&nbsp&nbsp&nbsp    Hàng hóa nhận lại phải đảm bảo đầy đủ bao bì, xốp,.. ( ngoại trừ film dán, màng nhựa…)<br>
&nbsp&nbsp&nbsp&nbsp    Hàng hóa khi nhận lại phải có đầy đủ các chứng từ kèm theo ( hóa đơn VAT, Phiếu bảo hành, Sách hướng dẫn )<br>

<b>VNBlackBerry</b> sẽ thực hiện đổi hàng cho khách, nhưng không hoàn lại phí vận chuyển/ giao hàng (nếu có), trừ những trường hợp sau:<br>

&nbsp&nbsp&nbsp&nbsp    Không đúng chủng loại, mẫu mã như quý khách đã đặt hàng.<br>
&nbsp&nbsp&nbsp&nbsp    Không đủ số lượng, không đủ bộ/ thông tin như trong đơn hàng ban đầu.<br>
&nbsp&nbsp&nbsp&nbsp    Không đạt chất lượng như: quá hạn sử dụng, hết bảo hành, không vận hành được, hỏng hóc khách quan trong phạm vi bảo hành của nhà cung cấp, nhà sản xuất,...<br><br>


